#include <stdio.h>

// 1. Описание функции SumRow
// A: Входной массив (матрица). M, N: Входные размеры. K: Входной индекс строки.
// Функция возвращает double (сумму).
double SumRow(double A[10][10], int M, int N, int K) {
    double Sum = 0.0;
    int j;

    // Проверка условия: если K выходит за пределы существующих строк (1 до M)
    if (K <= 0 || K > M) {
        return 0.0;
    }

    // Суммирование элементов K-й строки.
    // В задании индексы начинаются с 1, в C - с 0.
    // K-я строка соответствует индексу (K - 1)
    for (j = 0; j < N; j++) {
        // A[строка][столбец]
        Sum = Sum + A[K - 1][j];
    }

    return Sum;
}

int main() {
    int M, N; // Размеры матрицы
    int K;    // Индекс строки для суммирования
    int i, j;
    
    // Матрица объявлена с максимальным размером 10x10, как предписано
    double MatrixA[10][10]; 

    printf("--- Вычисление суммы элементов K-й строки матрицы ---\n\n");

    // --- Ввод размеров ---
    printf("Введите количество строк M (1-10): ");
    scanf("%d", &M);
    printf("Введите количество столбцов N (1-10): ");
    scanf("%d", &N);

    // Проверка ограничений (хотя в реальной жизни M, N могут быть больше 10)
    if (M > 10 || N > 10 || M < 1 || N < 1) {
        printf("Ошибка: Размеры M или N вне допустимого диапазона [1, 10].\n");
        return 1;
    }

    // --- Ввод элементов матрицы ---
    printf("\nВведите %d элементов матрицы построчно:\n", M * N);
    for (i = 0; i < M; i++) {
        for (j = 0; j < N; j++) {
            printf("A[%d][%d] (индекс 1: [%d][%d]): ", i, j, i + 1, j + 1);
            scanf("%lf", &MatrixA[i][j]);
        }
    }
    
    // --- Ввод K ---
    printf("\nВведите индекс K строки (начиная с 1), которую нужно суммировать (1 <= K <= %d): ", M);
    scanf("%d", &K);

    // --- Вызов функции ---
    double total_sum = SumRow(MatrixA, M, N, K);

    // --- Вывод результатов ---
    printf("\n--- Результат ---\n");
    if (K > M || K <= 0) {
        printf("Индекс K=%d некорректен (M=%d). Возвращено 0.0.\n", K, M);
    } else {
        printf("Сумма элементов в строке K=%d (индекс 1) матрицы %d x %d:\n", K, M, N);
        printf("Сумма = %.4f\n", total_sum);
    }

    return 0;
}

