#include <stdio.h>

// 1. Определение процедуры PowerA3
// A - входной параметр (вещественное)
// B - выходной параметр (передается по адресу, чтобы изменить значение вне функции)
void PowerA3(double A, double *B) {
    // Вычисление третьей степени
    *B = A * A * A;
}

int main() {
    // --- Выбор и представление данных ---
    
    // Массив из пяти входных чисел
    double InputData[5];
    // Массив для хранения пяти результатов
    double Results[5];
    
    // Переменная для временного хранения результата вызова процедуры
    double temp_result;
    int i;
    
    printf("--- Программа для вычисления третьей степени ---\n\n");
    
    // --- Ввод данных (Итерация 1: Ввод 5 чисел) ---
    printf("Введите 5 вещественных чисел:\n");
    for (i = 0; i < 5; i++) {
        printf("Число %d: ", i + 1);
        // Используем %lf для чтения double
        if (scanf("%lf", &InputData[i]) != 1) {
            printf("Ошибка ввода. Пожалуйста, введите число.\n");
            return 1; // Выход при ошибке
        }
    }
    
    // --- Вызов процедуры и обработка (Итерация 2: Вычисление кубов) ---
    for (i = 0; i < 5; i++) {
        // Вызываем процедуру: передаем входное значение (InputData[i]) 
        // и адрес переменной, куда нужно записать результат (&temp_result)
        PowerA3(InputData[i], &temp_result);
        
        // Сохраняем результат в массив результатов
        Results[i] = temp_result;
    }
    
    // --- Вывод результатов ---
    printf("\n--- Результаты вычислений ---\n");
    for (i = 0; i < 5; i++) {
        // Выводим исходное число и его куб
        printf("Число %.2f в третьей степени равно: %.4f\n", InputData[i], Results[i]);
    }
    
    return 0;
}

