#include <stdio.h>
#include <math.h> // Для использования функции sqrt()
#include <stdbool.h> // Для использования типа bool (true/false)

// Функция для проверки, является ли число простым
bool is_prime(int N) {
    // 1. Проверка граничных условий (хотя по заданию N > 1)
    if (N <= 1) {
        return false;
    }
    
    // 2. Проверка на 2 (единственное четное простое число)
    if (N == 2) {
        return true;
    }
    
    // 3. Проверка на все остальные четные числа
    if (N % 2 == 0) {
        return false;
    }
    
    // 4. Проверка нечетных делителей до корня из N
    // Мы проверяем только нечетные числа (i = 3, 5, 7, ...)
    int limit = (int)sqrt(N);
    
    for (int i = 3; i <= limit; i = i + 2) {
        if (N % i == 0) {
            // Нашли делитель, число составное
            return false;
        }
    }
    
    // Если цикл завершился, делителей не найдено
    return true;
}

int main() {
    int N;
    
    printf("--- Проверка числа на простоту ---\n");
    printf("Введите целое число N (N > 1): ");
    
    // Считывание входных данных
    if (scanf("%d", &N) != 1) {
        printf("Ошибка ввода.\n");
        return 1;
    }

    // Проверка условия N > 1
    if (N <= 1) {
        printf("FALSE (Число должно быть больше 1).\n");
        return 0;
    }
    
    // Вызов функции и вывод результата
    if (is_prime(N)) {
        printf("TRUE\n");
    } else {
        printf("FALSE\n");
    }
    
    return 0;
}

