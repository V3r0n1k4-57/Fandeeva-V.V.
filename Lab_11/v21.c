#include <stdio.h>

// Используем long long для предотвращения переполнения для N до ~92
typedef long long ll;

// 1. Определение функции Fib(N)
ll Fib(int N) {
    // Базовые случаи F1 = 1, F2 = 1
    if (N <= 0) {
        // Обработка некорректного ввода, хотя по условию N >= 1
        return 0; 
    }
    if (N == 1 || N == 2) {
        return 1;
    }
    
    // Рекурсивный шаг: Fk = Fk-2 + Fk-1
    return Fib(N - 2) + Fib(N - 1);
}

int main() {
    // --- Представление данных ---
    int InputN[5];         // Входные номера (int)
    ll Results[5];         // Выходные значения Фибоначчи (long long)
    int i;
    
    printf("--- Вычисление N-го числа Фибоначчи ---\n\n");
    
    // --- Ввод данных ---
    printf("Введите 5 номеров N (целые числа, например: 6 10 15 40 45):\n");
    for (i = 0; i < 5; i++) {
        printf("Номер %d: ", i + 1);
        // Читаем int
        if (scanf("%d", &InputN[i]) != 1 || InputN[i] <= 0) {
            printf("Ошибка ввода или номер <= 0. Пожалуйста, введите положительное целое число.\n");
            return 1;
        }
    }
    
    // --- Вызов функции и обработка ---
    for (i = 0; i < 5; i++) {
        // Вызов функции Fib и сохранение результата
        Results[i] = Fib(InputN[i]);
    }
    
    // --- Вывод результатов ---
    printf("\n--- Результаты ---\n");
    for (i = 0; i < 5; i++) {
        printf("F(%d) = %lld\n", InputN[i], Results[i]);
    }
    
    return 0;
}

