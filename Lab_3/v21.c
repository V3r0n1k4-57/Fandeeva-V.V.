#include <stdio.h>
#include <stdbool.h> // Подключаем для использования типа bool (true/false)

// Функция для проверки условия возрастания
bool check_increasing_sequence(int N) {
    // Проверка диапазона (если входные данные могут быть некорректными)
    if (N < 100 || N > 999) {
        // Если число не трехзначное, условие возрастания цифр не может быть выполнено по определению задачи
        return false; 
    }

    // 1. Выделение цифр
    int d1 = N / 100;          // Сотни
    int d2 = (N % 100) / 10;   // Десятки
    int d3 = N % 10;           // Единицы

    // 2. Проверка условия: d1 < d2 И d2 < d3
    if (d1 < d2 && d2 < d3) {
        return true; // Истинно
    } else {
        return false; // Ложно
    }
}

int main() {
    int number;
    
    printf("--- Проверка возрастающей последовательности цифр трехзначного числа ---\n");
    printf("Введите трехзначное число (N): ");
    
    // Ввод данных
    if (scanf("%d", &number) != 1) {
        printf("Ошибка ввода. Пожалуйста, введите целое число.\n");
        return 1;
    }

    // Вызов функции
    bool result = check_increasing_sequence(number);

    // Вывод результата
    printf("\nВходное число: %d\n", number);
    printf("Высказывание 'Цифры образуют возрастающую последовательность' является: ");
    
    if (result) {
        printf("TRUE\n");
    } else {
        printf("FALSE\n");
    }

    return 0;
}

