#include <stdio.h>

// Определение константы: количество секунд в одном часе
#define SECONDS_PER_HOUR 3600

int main() {
    // 1. Выбор и представление данных
    // N - Входное данное (целое число секунд)
    int N; 
    // H - Выходное данное (целое число часов)
    int H; 

    // --- Ввод данных ---
    printf("--- Расчет целых часов по прошедшим секундам ---\n");
    printf("Введите общее количество прошедших секунд N (целое число): ");
    
    // Считываем входное значение N
    if (scanf("%d", &N) != 1) {
        printf("Ошибка ввода. Введено некорректное значение.\n");
        return 1; // Возвращаем код ошибки
    }

    // Проверка на корректность (не более секунд в сутках)
    if (N < 0 || N >= 86400) {
        printf("Предупреждение: Введено значение вне диапазона одного дня (0 до 86399).\n");
        // Продолжаем расчет, но предупреждаем пользователя
    }

    // --- Алгоритм и Обработка ---
    // 2. Вычисление: Используем целочисленное деление. 
    // В языке C, когда оба операнда (N и 3600) являются целыми числами, 
    // результат деления автоматически отбрасывает дробную часть (аналог функции floor()).
    H = N / SECONDS_PER_HOUR;

    // --- Вывод данных ---
    printf("\n--- Результат ---\n");
    printf("Прошло секунд: %d\n", N);
    printf("Прошло ЦЕЛЫХ часов: %d\n", H);
    
    return 0; // Успешное завершение программы
}

