#include <stdio.h>

// Максимальный размер массива (для статического выделения, чтобы избежать ошибок новичков с malloc)
#define MAX_SIZE 100

/**
 * @brief Находит индексы двух одинаковых элементов в массиве.
 * 
 * Используется алгоритм полного перебора (O(N^2)).
 * 
 * @param arr Массив целых чисел.
 * @param N Размер массива.
 */
void find_duplicates(int arr[], int N) {
    int index1 = -1;
    int index2 = -1;
    int found = 0; 

    // --- Шаг 4: Внешний цикл (Индекс i) ---
    // Перебираем от 0 до N-2
    for (int i = 0; i < N - 1; i++) {
        
        // --- Шаг 5: Внутренний цикл (Индекс j) ---
        // Начинаем с элемента, следующего за i (j = i + 1)
        for (int j = i + 1; j < N; j++) {
            
            // --- Шаг 6: Сравнение ---
            if (arr[i] == arr[j]) {
                
                // --- Шаг 7: Действие при совпадении ---
                index1 = i;
                index2 = j;
                found = 1;
                
                // Выход из внутреннего цикла, так как пара найдена
                break; 
            }
        }
        
        // Если пара найдена, выходим и из внешнего цикла, чтобы избежать лишних проверок
        if (found) {
            break;
        }
    }

    // Вывод результата
    if (found) {
        // Поскольку j всегда начинался с i + 1, index1 < index2 гарантировано
        printf("Результат: Индексы дубликатов: %d и %d\n", index1, index2);
    } else {
        // По условию задачи это не должно произойти
        printf("Ошибка: Дублирующая пара не найдена.\n");
    }
}

int main() {
    // --- Шаг 2: Ввод данных ---
    int N;
    int A[MAX_SIZE];

    printf("--- Программа поиска дубликатов в массиве ---\n");
    
    // Ввод размера N
    printf("Введите размер массива N (2 <= N <= %d): ", MAX_SIZE);
    if (scanf("%d", &N) != 1 || N < 2 || N > MAX_SIZE) {
        printf("Ошибка: Некорректное значение N.\n");
        return 1;
    }

    // Ввод элементов массива
    printf("Введите %d целых чисел:\n", N);
    for (int i = 0; i < N; i++) {
        printf("A[%d]: ", i);
        if (scanf("%d", &A[i]) != 1) {
            printf("Ошибка ввода элемента.\n");
            return 1;
        }
    }

    printf("\n--- Запуск поиска ---\n");
    
    // Вызов функции решения
    find_duplicates(A, N);

    return 0;
}


