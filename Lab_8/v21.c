#include <stdio.h>

// Ограничение размера N по условию задачи (2 <= N <= 10)
#define MAX_N 10 

/**
 * @brief Находит индексы двух одинаковых элементов в массиве, используя полный перебор.
 * 
 * @param arr Массив целых чисел.
 * @param N Фактический размер массива.
 */
void find_duplicates(int arr[], int N) {
    int index0 = -1; // 0-индекс первого найденного элемента
    int index1 = -1; // 0-индекс второго найденного элемента
    int found = 0; 

    // Полный перебор (O(N^2))
    for (int i = 0; i < N - 1; i++) {
        for (int j = i + 1; j < N; j++) {
            
            // Сравнение элементов
            if (arr[i] == arr[j]) {
                index0 = i;
                index1 = j;
                found = 1;
                break; // Выход из внутреннего цикла
            }
        }
        if (found) {
            break; // Выход из внешнего цикла
        }
    }

    // Вывод результата с учетом 1-индексации
    if (found) {
        // index0 + 1 и index1 + 1 дают нам номера элементов, начинающиеся с 1
        printf("Найдены дубликаты:\n");
        printf("Номер 1: %d\n", index0 + 1);
        printf("Номер 2: %d\n", index1 + 1);
    } else {
        // Этого по условию задачи не должно произойти
        printf("Ошибка: Дубликат не найден.\n");
    }
}

int main() {
    int N;
    int A[MAX_N]; // Используем статический массив максимального размера

    printf("--- Поиск дубликатов (1-индексация) ---\n");
    
    // Ввод размера N
    printf("Введите размер массива N (2 <= N <= %d): ", MAX_N);
    if (scanf("%d", &N) != 1 || N < 2 || N > MAX_N) {
        printf("Ошибка: Некорректное значение N.\n");
        return 1;
    }

    // Ввод элементов массива
    printf("Введите %d целых чисел:\n", N);
    for (int i = 0; i < N; i++) {
        printf("Элемент A[%d] (0-индекс): ", i);
        if (scanf("%d", &A[i]) != 1) {
            printf("Ошибка ввода элемента.\n");
            return 1;
        }
    }

    printf("\n--- Запуск алгоритма ---\n");
    
    find_duplicates(A, N);

    return 0;
}

