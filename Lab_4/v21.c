#include <stdio.h>

// Функция для решения задачи
int solve_point_location(int x, int y) {
    // 1. Проверка на начало координат (0, 0)
    if (x == 0 && y == 0) {
        return 0;
    }
    // 2. Проверка на ось OX (y = 0, но x не 0, так как случай (0,0) уже проверен)
    else if (y == 0) {
        return 1;
    }
    // 3. Проверка на ось OY (x = 0, но y не 0)
    else if (x == 0) {
        return 2;
    }
    // 4. Если ни одно из условий выше не выполнилось (x != 0 И y != 0)
    else {
        return 3;
    }
}

int main() {
    int x, y;
    int result;

    // --- Выбор и обоснование представления данных:
    // x, y - int (входные целые координаты)
    // result - int (выходной код)

    printf("--- Программа определения положения точки на плоскости ---\n");

    // Ввод данных
    printf("Введите координату X (целое число): ");
    if (scanf("%d", &x) != 1) {
        printf("Ошибка ввода X.\n");
        return 1;
    }

    printf("Введите координату Y (целое число): ");
    if (scanf("%d", &y) != 1) {
        printf("Ошибка ввода Y.\n");
        return 1;
    }

    // Вызов функции для получения результата
    result = solve_point_location(x, y);

    // Вывод результата
    printf("\nКоординаты точки: (%d, %d)\n", x, y);
    printf("Код результата: %d\n", result);

    // Пояснение результата (для наглядности)
    switch (result) {
        case 0:
            printf("Точка совпадает с началом координат.\n");
            break;
        case 1:
            printf("Точка лежит на оси OX.\n");
            break;
        case 2:
            printf("Точка лежит на оси OY.\n");
            break;
        case 3:
            printf("Точка не лежит на координатных осях.\n");
            break;
    }

    return 0;
}

